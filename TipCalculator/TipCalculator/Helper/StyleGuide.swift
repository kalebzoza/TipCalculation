import UIKit


extension UIColor {
    static func backgroundColor() -> UIColor {
        return UIColor(red: 23/255, green: 42/255, blue: 56/255, alpha: 1)
    }
    
    static func primaryColor() -> UIColor {
        return UIColor(red: 85/255, green: 156/255, blue: 207/255, alpha: 1)
    }
    
    static func secondaryColor() -> UIColor {
        return UIColor(red: 45/255, green: 83/255, blue: 110/255, alpha: 1)
    }
}
